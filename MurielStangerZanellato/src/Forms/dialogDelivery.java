    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.carregaCardapio();
        this.itens();
        this.inicializa();

        Timer timer = new Timer();
        timer.schedule(new TimerTask() {
            @Override
            public void run() {
                for (Nodo nodo = lista.getPrimeiro(); nodo != null; nodo = nodo.getAnterior()) {
                    nodo.getPedido().setTotalTempo(nodo.getPedido().getTotalTempo() - 1);
                    if (nodo.getPedido().getTotalTempo() <= 0) { 
                        listaPedidosAtendidos.add("Pedido" + nodo.getPedido().getPedido() + " atendido."); 
                        lista.removeInicio(); 
                        pedidosAtendidos(); 
                    }
                 
                }
                pedidos();
            }
        }, 0, 1000);


    }//GEN-LAST:event_formWindowOpened

    private void ButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAdicionarActionPerformed
        Cardapio item = (Cardapio) ComboCardapio.getSelectedItem();
        pedido.getItensCardapio().add(item);
        TextValor.setText(Double.parseDouble(TextValor.getText()) + item.getPreco() + "");
        TextTempo.setText(Integer.parseInt(TextTempo.getText()) + item.getTempoMinutos() + "");
        this.itens();
    }//GEN-LAST:event_ButtonAdicionarActionPerformed

    private void ListItensMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ListItensMouseClicked
        if (evt.getClickCount() == 2) {
            Cardapio item = (Cardapio) ListItens.getSelectedValue();
            if (JOptionPane.showConfirmDialog(null, "Retirar " + item + " do cardapio?") != 0) {
                return;
            }

            pedido.getItensCardapio().remove(item);
            TextValor.setText(Double.parseDouble(TextValor.getText()) - item.getPreco() + "");
            TextTempo.setText(Integer.parseInt(TextTempo.getText()) - item.getTempoMinutos() + "");
            this.itens();
        }
    }//GEN-LAST:event_ListItensMouseClicked

    private void ButtonFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonFinalizarActionPerformed
        if (pedido.getItensCardapio().size() == 0) {
            JOptionPane.showMessageDialog(null, "Pedido sem itens!");
            return;
        }

        pedido.setDataHoraPedido(LocalDateTime.now());
        pedido.setTotalTempo(Integer.parseInt(TextTempo.getText()));
        pedido.setTotalPreco(Double.parseDouble(TextValor.getText()));

        lista.insereOrdenado(pedido);
        this.inicializa();
        this.pedidos();
    }//GEN-LAST:event_ButtonFinalizarActionPerformed

