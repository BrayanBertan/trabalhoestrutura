    public void carregaCardapio() {
        DefaultComboBoxModel dcm = new DefaultComboBoxModel(Cardapio.values());
        ComboCardapio.setModel(dcm);
    }

    public void pedidos() {
        LinkedList<Pedido> listaPedidos = new LinkedList<>();

        for (Nodo nodo = lista.getPrimeiro(); nodo != null; nodo = nodo.getAnterior()) {
            listaPedidos.add(nodo.getPedido());
        }
        DefaultListModel dlm = new DefaultListModel();
        dlm.addAll(listaPedidos);
        ListPedidos.setModel(dlm);
    }

    public void pedidosAtendidos() {
        DefaultListModel dlm = new DefaultListModel();
        dlm.addAll(listaPedidosAtendidos);
        ListaAtendidos.setModel(dlm);
    }

    public void itens() {
        DefaultListModel dlm = new DefaultListModel();
        dlm.addAll(pedido.getItensCardapio());
        ListItens.setModel(dlm);
    }

    public void inicializa() {
        TextTempo.setText("0");
        TextValor.setText("0");
        ComboCardapio.setSelectedIndex(0);
        pedido = new Pedido();
        itens();
        ComboCardapio.requestFocus();
    }

